    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String spot = jComboBox1.getSelectedItem().toString();
        
        if (spot == "Savin Kingdom"){
        	jTextField1.setText("Savin Kingdom");
        	jTextField4.setText("Amusement park in Siliguri");
        	jTextField3.setText("Darjeeling More, Dagapur, Siliguri, West Bengal 734003");
        }
        if (spot == "Mahananda Wildlife Sanctuary"){
        	jTextField1.setText("Mahananda Wildlife Sanctuary");
        	jTextField4.setText("A reserved forest area that initially started as a game sanctuary where children took rides.");
        	jTextField3.setText("Shukhna, Near Darjeeling");
        }
        if (spot == "Siliguri Science City"){
        	jTextField1.setText("Siliguri Science City");
        	jTextField4.setText("An educational park and people of all ages can come here and learn science through experiment.");
        	jTextField3.setText("Near Maynaguri, Saidpur");
        }
        if (spot == "Hong Kong Market"){
        	jTextField1.setText("Hong Kong Market");
        	jTextField3.setText("Bidhan Market Rd, Ward 11, Hakim Para, Siliguri, West Bengal 734001");
        	jTextField4.setText("Flea Market");
        }
        
        if (spot == "Coronation Bridge"){
        	jTextField1.setText("Coronation Bridge");
        	jTextField4.setText("Teesta River, Jalpaiguri, Dist Darjeeling, West Bengal");
        	jTextField3.setText("Given its architectural and historical value, the bridge should be declared a heritage site.");
        }
        if (spot == "Iskcon Mandir"){
        	jTextField1.setText("Iskcon Mandir");
        	jTextField4.setText("Hindu temple in Siliguri");
        	jTextField3.setText("ISKCON Temple Road, Iskcon Rd, Gitalpara, Siliguri, West Bengal 734406");
        }
    }
    
